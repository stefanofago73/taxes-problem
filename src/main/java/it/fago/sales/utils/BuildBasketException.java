package it.fago.sales.utils;

public class BuildBasketException extends RuntimeException {

	/**
	 * 
	 */
	private static final long serialVersionUID = -779055721619510602L;

	public BuildBasketException() {
		super();
	}

	public BuildBasketException(String message, Throwable cause) {
		super(message, cause);
	}

	public BuildBasketException(String message) {
		super(message);
	}

	public BuildBasketException(Throwable cause) {
		super(cause);
	}

}// END
